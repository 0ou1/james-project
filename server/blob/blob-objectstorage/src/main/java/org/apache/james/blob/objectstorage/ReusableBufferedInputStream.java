package org.apache.james.blob.objectstorage;

import java.io.BufferedInputStream;
import java.io.InputStream;

class ReusableBufferedInputStream extends BufferedInputStream {
    private static final int MINIMUM_ACCEPTABLE_ARRAY_SIZE = 1;

    ReusableBufferedInputStream(InputStream in, byte[] buffer) {
        super(in, MINIMUM_ACCEPTABLE_ARRAY_SIZE);
        this.buf = buffer;
    }
}
